#!/usr/bin/env npx tsx
/**
 * Interactive Smart Home Setup Script
 *
 * Sets up direct integrations with:
 * - Ecobee (thermostat)
 * - Philips Hue (lights)
 * - LIFX (lights)
 *
 * Run: pnpm smart-home:setup
 */

import * as fs from 'fs';
import * as path from 'path';
import * as readline from 'readline';

const ENV_FILE = path.join(process.cwd(), '.env.local');

// =============================================================================
// UTILITIES
// =============================================================================

function createReadline(): readline.Interface {
  return readline.createInterface({
    input: process.stdin,
    output: process.stdout,
  });
}

async function prompt(rl: readline.Interface, question: string): Promise<string> {
  return new Promise((resolve) => {
    rl.question(question, (answer) => resolve(answer.trim()));
  });
}

async function promptYesNo(rl: readline.Interface, question: string): Promise<boolean> {
  const answer = await prompt(rl, `${question} (y/n): `);
  return answer.toLowerCase().startsWith('y');
}

function loadEnvFile(): Record<string, string> {
  const env: Record<string, string> = {};
  if (fs.existsSync(ENV_FILE)) {
    const content = fs.readFileSync(ENV_FILE, 'utf-8');
    for (const line of content.split('\n')) {
      const match = line.match(/^([^#=]+)=(.*)$/);
      if (match) {
        env[match[1].trim()] = match[2].trim();
      }
    }
  }
  return env;
}

function saveEnvFile(env: Record<string, string>): void {
  const lines: string[] = [
    '# Smart Home Integration Configuration',
    '# Generated by pnpm smart-home:setup',
    '',
  ];

  // Group by category
  const categories = {
    'Test User': ['TEST_USER_ID'],
    'Ecobee': ['ECOBEE_API_KEY'],
    'Philips Hue': ['HUE_BRIDGE_IP', 'HUE_USERNAME'],
    'LIFX': ['LIFX_TOKEN'],
    'Home Assistant (Optional)': ['HOME_ASSISTANT_URL', 'HOME_ASSISTANT_TOKEN'],
    'Test Options': ['DRY_RUN', 'VERBOSE', 'SKIP_RESTORE'],
  };

  for (const [category, keys] of Object.entries(categories)) {
    const hasAny = keys.some((k) => env[k]);
    if (hasAny) {
      lines.push(`# ${category}`);
      for (const key of keys) {
        if (env[key]) {
          lines.push(`${key}=${env[key]}`);
        }
      }
      lines.push('');
    }
  }

  // Add any other keys not in categories
  const allCategoryKeys = Object.values(categories).flat();
  for (const [key, value] of Object.entries(env)) {
    if (!allCategoryKeys.includes(key) && value) {
      lines.push(`${key}=${value}`);
    }
  }

  fs.writeFileSync(ENV_FILE, lines.join('\n'));
  console.log(`\n✅ Saved to ${ENV_FILE}\n`);
}

