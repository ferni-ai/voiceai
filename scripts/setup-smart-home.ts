async function setupEcobee(rl: readline.Interface, env: Record<string, string>): Promise<void> {
  console.log('\n' + '═'.repeat(60));
  console.log('🌡️  ECOBEE THERMOSTAT SETUP');
  console.log('═'.repeat(60));

  if (env.ECOBEE_API_KEY) {
    console.log(`\n✅ Already configured: ${env.ECOBEE_API_KEY.slice(0, 8)}...`);
    const reconfigure = await promptYesNo(rl, 'Reconfigure?');
    if (!reconfigure) return;
  }

  console.log('\n📋 Steps to get your Ecobee API Key:\n');
  console.log('   1. Go to: https://www.ecobee.com/developers/');
  console.log('   2. Sign in with your Ecobee account');
  console.log('   3. Click "Create New" to register an app');
  console.log('   4. Name: "Ferni" (or anything)');
  console.log('   5. Copy the API Key\n');

  const apiKey = await prompt(rl, 'Enter your Ecobee API Key (or press Enter to skip): ');

  if (apiKey) {
    env.ECOBEE_API_KEY = apiKey;
    console.log('\n✅ Ecobee API Key saved!');
    console.log('   Next step: Run "pnpm ecobee:auth" to complete authorization');
  } else {
    console.log('   Skipped Ecobee setup');
  }
}

