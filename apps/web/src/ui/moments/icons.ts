/**
 * Get icon SVG by name with optional size.
 */
export function getIcon(name: MomentIconName, size: number = 24): string {
  const svg = MOMENT_ICONS[name];
  if (!svg) {
    return MOMENT_ICONS.sparkle; // Fallback
  }

  // Replace viewBox to add width/height
  return svg.replace(
    'viewBox="0 0 24 24"',
    `viewBox="0 0 24 24" width="${size}" height="${size}"`
  );
}

/**
 * Create icon element.
 */
export function createIconElement(name: MomentIconName, size: number = 24): HTMLElement {
  const container = document.createElement('span');
  container.className = 'moment-icon';
  container.innerHTML = getIcon(name, size);
  return container;
}
