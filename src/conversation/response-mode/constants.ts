/**
 * Patterns indicating venting/emotional release
 */
export const VENTING_PATTERNS = {
  /** High-intensity venting markers */
  highIntensity: [
    /I (just )?(can't|cannot) (believe|stand|take|handle)/i,
    /I('m| am) so (frustrated|angry|upset|annoyed|tired|exhausted|sick)/i,
    /This is (ridiculous|insane|unbelievable|crazy)/i,
    /(hate|despise|loathe) (this|that|it|them|him|her)/i,
    /I've (had|have) (enough|it)/i,
    /What the (hell|heck|f)/i,
    /Are you (kidding|serious)/i,
  ],
  /** Moderate venting markers */
  moderateIntensity: [
    /I('m| am) (feeling )?(so )?(overwhelmed|stressed|anxious)/i,
    /I('m| am) so (overwhelmed|stressed|anxious|frustrated|tired)/i,
    /It('s| is) (really|so|just) (hard|difficult|tough)/i,
    /I (don't|do not) know (what to do|how to|anymore)/i,
    /Everything is (going wrong|falling apart|a mess)/i,
    /I('m| am) (struggling|having trouble) with/i,
  ],
  /** Length indicator - long messages often indicate venting */
  minLengthForVenting: 50,
};

/**
 * Patterns indicating vulnerability
 */
export const VULNERABILITY_PATTERNS = {
  /** High vulnerability markers */
  high: [
    /I('ve| have) never told (anyone|somebody|anyone else)/i,
    /This is (hard|difficult|scary) (to say|to admit|to share)/i,
    /I('m| am) (scared|afraid|terrified) (to|of|that)/i,
    /I feel (so )?(alone|lonely|isolated)/i,
    /I('m| am) (ashamed|embarrassed) (of|to|that)/i,
    /Please don't (judge|tell|think)/i,
  ],
  /** Medium vulnerability markers */
  medium: [
    /I('ve| have) been (thinking|wondering|worried)/i,
    /I (don't|do not) (usually|normally) (talk about|share|mention)/i,
    /Can I (tell|ask|share|say) you something/i,
    /Between (you and me|us)/i,
    /I trust you/i,
  ],
  /** Low vulnerability markers */
  low: [/I('m| am) not sure (if|how|whether)/i, /I guess/i, /I think/i, /Maybe/i],
};

/**
 * Patterns for question detection
 */
export const QUESTION_PATTERNS = {
  /** Direct questions */
  direct: [
    /^(what|where|when|why|who|how|which|whose|whom)\b/i,
    /\?$/,
    /^(do|does|did|is|are|was|were|can|could|will|would|should|have|has|had)\b/i,
  ],
  /** Indirect questions */
  indirect: [
    /I('m| am) wondering/i,
    /I('d| would) like to know/i,
    /Can you (tell|explain|help)/i,
    /Could you/i,
    /I was hoping you could/i,
  ],
  /** Rhetorical questions (don't need answer) */
  rhetorical: [
    /you know\?$/i,
    /right\?$/i,
    /isn't it\?$/i,
    /don't you think\?$/i,
    /what (even|the hell|the heck) is/i,
  ],
};

// ============================================================================
// THRESHOLDS
