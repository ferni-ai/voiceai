export const THRESHOLDS = {
  /** Minimum intensity to consider high-intensity venting */
  highIntensityVenting: 0.7,

  /** Minimum intensity for moderate venting */
  moderateIntensityVenting: 0.4,

  /** Minimum words to consider "short" turn */
  shortTurnWords: 10,

  /** Minimum words to consider "long" turn */
  longTurnWords: 50,

  /** Maximum consecutive brief modes before switching */
  maxConsecutiveBrief: 3,

  /** Minimum confidence for mode decision */
  minConfidence: 0.6,
};
